package day06_arithmetic_operators;

public class DifferentTypes {
    public static void main(String[] args) {


        // both int
        System.out.println(10 - 5);
        System.out.println(10 * 5);

        // double/int --> double
        System.out.println(10.0 / 5);

        // double/ double --> double
        System.out.println(10.0 / 5.0);
    }
}
